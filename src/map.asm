INCLUDE "constants.inc"
SECTION "MapCode", ROM0


DEF SECTION_WIDTH EQU 32
DEF SECTION_HEIGHT EQU 18
DEF TILES_PER_SECTION EQU SECTION_HEIGHT*SECTION_WIDTH
DEF Map_Buffer_Address EQU $C400

initMap::
    call loadBackgroundTiles
    ret

loadBufferMapIntoVRAM:
    ld d, $4
    ld e, 0
    ld b, HIGH(Map_Buffer_Address)
    ld c, LOW(Map_Buffer_Address)
    ld h, HIGH(_VRAM_TILEMAP)
    ld l, LOW(_VRAM_TILEMAP)
    call memcpy
    ret

loadBackgroundTiles:
    ld de, Tiles
    ld hl, _VRAM_TILES_BACKGROUND
	ld bc, TilesEnd - Tiles
.copy:
	ld a, [de]
	ld [hli], a
	inc de
	dec bc
	ld a, b
	or a, c
	jp nz, .copy
    ret

resetMap::
    ld a, 0 
    ld [scrollCount],a
    call loadTileMap
    call loadBufferMapIntoVRAM
    ret

loadTileMap::
    ld bc, _TILES_ON_SCREEN
    ld de, TileMap
    ld hl, Map_Buffer_Address  ;_VRAM_TILEMAP

.copy:
    ld a, [de]
    add a, _BG_TILES_START_INDEX
    ld[hli], a
    inc de
    dec bc
    ld a, b
	or a, c
	jp nz, .copy

    ld hl, TileMap
    ld b, 0
    ld c, 26
    add hl, bc
    ld a, h
    ld [TILEMAP_CURRENT_ROW], a
    ld a, l
    ld [TILEMAP_CURRENT_ROW+1], a
    ret

updateMap::
    ld a, [rSCX]
    add a, 2
    ld [rSCX], a
	ld a, [scrollCount]
    add a, 2
    ld [scrollCount], a
    cp a, 8
    jp nz, .skipUpdate
        call loadNextColumn
        ld a, 0
        ld [scrollCount], a
        ld a, 1 
        ld [DRAW_ME], a
    .skipUpdate:
    ret


loadNextColumn:
    ld hl, Map_Buffer_Address
    ld b, 0 
    ld a, [rSCX]
    add a, 200
    srl a
    srl a
    srl a
    ld [BUFFER_ROW_TO_DRAW], a
    ld c, a
    add hl, bc
    ld a, [TILEMAP_CURRENT_ROW]
    ld d, a
    ld a, [TILEMAP_CURRENT_ROW+1]
    ld e, a

    ld b, SECTION_HEIGHT
   
    
    .loop:
        ld c, SECTION_WIDTH
        ld a, [de]
        add a, _BG_TILES_START_INDEX
        ld [hl], a
        ld a, b
        ld b, 0
        add hl, bc
        ld b, h
        ld c, l
        ld h, d
        ld l, e
        ld d, 0 
        ld e, 32
        add hl, de
        ld d, h
        ld e, l
        ld h, b
        ld l , c

        ld b, a
        dec b
        jp nz, .loop
    ld a, [TILEMAP_CURRENT_ROW]
    ld d, a
    ld a, [TILEMAP_CURRENT_ROW+1]
    ld e, a
    inc de
    ld a, l
    cp a, $5F

    jp nz, .noSectionTransition
        ld hl, 544
        add hl, de
        ld d, h
        ld e, l
    .noSectionTransition:
    ld a, d
    ld [TILEMAP_CURRENT_ROW], a
    ld a, e
    ld [TILEMAP_CURRENT_ROW+1], a

    ret

getTileId:: ;x in a, y in b
    ld h, 0
    ld l, 0
    ld d, 0
    ld e, 32
    .loop:
        cp a, 0 
        jp z, .end
        add hl, de
        dec a
        jp .loop
    .end:
    ld e, b
    add hl, de
    
    ld de, Map_Buffer_Address
    add hl, de
    ld a, [hl]
    ret

isSolid:: ;id in a, and return bool in a
    ld de, SolidsEnd - Solids
    ld hl, Solids
    ld b, 0
    ld c, a
    .loop:
        ld a, [hli]
        cp a, c
        jp nz, .noMatch
            ld a, 1
            jp .end
        .noMatch:
        dec de
        ld a, e
        cp a, 0
        jp nz, .loop
        ld a, d
        cp a, 0
        jp nz, .loop

    .end:
    ret

loadRowFromBuffer::
    ld hl, Map_Buffer_Address
    ld de, _VRAM_TILEMAP
    ld a, [BUFFER_ROW_TO_DRAW]
    ld c, a
    add a, l
    ld l, a
    ld a, c
    add a, e
    ld e, a
    ld b, SECTION_HEIGHT    
    
    .loop
        ld a, [hl]
        ld [de], a
        ld a, d
        ld c, e
        ld d, 0 
        ld e, 32
        add hl, de
        ld d, a
        ld e, c
        ld a, h
        ld c, l
        ld h, 0
        ld l, 32
        add hl, de
        ld d, h
        ld e, l
        ld h, a
        ld l, c
        ld a, b
        cp a, 0
        jp z, .end
        dec a
        ld b, a
        jp .loop

    .end:     



    ret

