    ld de, TileMap
    ld hl, _VRAM_TILEMAP

.copy:
    ld a, [de]
    add a, _BG_TILES_START_INDEX
    ld[hli], a
    inc de
    dec bc
    ld a, b
	or a, c
	jp nz, .copy

    ld hl, TileMap
    ld b, 0
    ld c, 26
