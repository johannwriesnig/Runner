updateMap::
    ld a, [scroll_count]
    cp a, 8
    jp nz, .skipUpdate
        call loadNextColumn
        ld a, 0
        ld [scroll_count], a
    .skipUpdate:
    ret


loadNextColumn:
    ld hl, _VRAM_TILEMAP
    ld b, 0 
    ld a, [rSCX]
    add a, 200
    srl a
    srl a
    srl a
    ld c, a
    add hl, bc
    ld a, [_map_latest_drawn_colum_address]
    ld d, a
    ld a, [_map_latest_drawn_colum_address+1]
    ld e, a

    ld a, _TILES_IN_ROW
    ld b, a
   
    
    .loop:
        ld c, _TILES_IN_ROW
        ld a, [de]
        add a, _BG_TILES_START_INDEX
        ld [hl], a
        ld a, b
        ld b, 0
        add hl, bc
        ld b, h
        ld c, l
        ld h, d
        ld l, e
        ld d, 0 
        ld e, 32
        add hl, de
        ld d, h
        ld e, l
        ld h, b
        ld l , c

        ld b, a
        dec b
        jp nz, .loop
    ld a, [_map_latest_drawn_colum_address]
    ld d, a
    ld a, [_map_latest_drawn_colum_address+1]
    ld e, a
    inc de
    ld a, l
    cp a, $1F

    jp nz, .noSectionTransition
        ld hl, 992
        add hl, de
        ld d, h
        ld e, l
    .noSectionTransition:
    ld a, d
    ld [_map_latest_drawn_colum_address], a
    ld a, e
    ld [_map_latest_drawn_colum_address+1], a

    ret

